// Write a program to check whether a given number is even or odd.
// Hint: Use modulus operator %.
// Sample Input/Output:

// Input: 7 → Output: Odd
// Input: 10 → Output: Even


let number = 10 ;

if(number % 2 === 0){
    console.log(number, "is even");
}else{
    console.log(number,"is odd");
}